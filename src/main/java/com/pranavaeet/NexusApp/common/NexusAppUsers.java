package com.pranavaeet.NexusApp.common;

public class NexusAppUsers {
	private String userId;
	private String userName;
	private String userFullName;
	private String userRole;
	private String userMobile;
	private String userEmail;
	private String userAddress;
	private String city;
	private String state;
	private String country;
	private String zipcode;
	private String status;
	private String image;

	public NexusAppUsers() {

	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserFullName() {
		return userFullName;
	}

	public void setUserFullName(String userFullName) {
		this.userFullName = userFullName;
	}

	public String getUserRole() {
		return userRole;
	}

	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}

	public String getUserMobile() {
		return userMobile;
	}

	public void setUserMobile(String userMobile) {
		this.userMobile = userMobile;
	}

	public String getUserEmail() {
		return userEmail;
	}

	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}

	public String getUserAddress() {
		return userAddress;
	}

	public void setUserAddress(String userAddress) {
		this.userAddress = userAddress;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getZipcode() {
		return zipcode;
	}

	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	@Override
	public String toString() {
		return "NexusAppUsers [userId=" + userId + ", userName=" + userName + ", userFullName=" + userFullName
				+ ", userRole=" + userRole + ", userMobile=" + userMobile + ", userEmail=" + userEmail
				+ ", userAddress=" + userAddress + ", city=" + city + ", state=" + state + ", country=" + country
				+ ", zipcode=" + zipcode + ", status=" + status + ", image=" + image + "]";
	}


}
